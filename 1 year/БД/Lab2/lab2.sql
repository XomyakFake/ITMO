SELECT Н_ТИПЫ_ВЕДОМОСТЕЙ.НАИМЕНОВАНИЕ, Н_ВЕДОМОСТИ.ДАТА 
FROM Н_ТИПЫ_ВЕДОМОСТЕЙ 
